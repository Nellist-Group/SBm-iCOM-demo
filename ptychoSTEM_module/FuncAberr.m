function func_aber = FuncAberr(aberr,K,phi)

% this function needs comments

func_aber = 1/2 .* K.^2 .* aberr.df ...
    + 1/2 * aberr.C12.*cos(2*(phi+(aberr.C12phi*pi/180))) .* K.^2 ...
    + 1/3 * aberr.C21.*cos(1*(phi+(aberr.C21phi*pi/180))) .* K.^3 ...
    + 1/3 * aberr.C23.*cos(3*(phi+(aberr.C23phi*pi/180))) .* K.^3 ...
    + 1/4 * aberr.C30                                     .* K.^4 ...
    + 1/4 * aberr.C32.*cos(2*(phi+(aberr.C32phi*pi/180))) .* K.^4 ...
    + 1/4 * aberr.C34.*cos(4*(phi+(aberr.C34phi*pi/180))) .* K.^4 ...
    + 1/6 * aberr.C50                                     .* K.^6 ...
    + 1/6 * aberr.C56.*cos(6*(phi+(aberr.C56phi*pi/180))) .* K.^6 ;

end

